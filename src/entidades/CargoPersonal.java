package entidades;

public class CargoPersonal {

	private int id_cargo_personal;
	private int estado;
	private int id_cargo;
	private int id_personal;
	
	public int getId_cargo_personal() {
		return id_cargo_personal;
	}
	public void setId_cargo_personal(int id_cargo_personal) {
		this.id_cargo_personal = id_cargo_personal;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	public int getId_cargo() {
		return id_cargo;
	}
	public void setId_cargo(int id_cargo) {
		this.id_cargo = id_cargo;
	}
	public int getId_personal() {
		return id_personal;
	}
	public void setId_personal(int id_personal) {
		this.id_personal = id_personal;
	}
	
	
}
