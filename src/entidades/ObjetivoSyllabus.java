package entidades;

public class ObjetivoSyllabus {
	private int id_objetivo;
	private int id_syllabus;
	private String objetivo;
	
	public int getId_objetivo() {
		return id_objetivo;
	}
	public void setId_objetivo(int id_objetivo) {
		this.id_objetivo = id_objetivo;
	}
	public int getId_syllabus() {
		return id_syllabus;
	}
	public void setId_syllabus(int id_syllabus) {
		this.id_syllabus = id_syllabus;
	}
	public String getObjetivo() {
		return objetivo;
	}
	public void setObjetivo(String objetivo) {
		this.objetivo = objetivo;
	}
	
}
