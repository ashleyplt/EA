package vistas;

public class VW_CoordinacionCarrera {
	private int id_coordinacion;
	private String coordinacion;
	private String telefono;
	private String carrera;
	private int estado;
	
	public int getId_coordinacion() {
		return id_coordinacion;
	}
	public void setId_coordinacion(int id_coordinacion) {
		this.id_coordinacion = id_coordinacion;
	}
	public String getCoordinacion() {
		return coordinacion;
	}
	public void setCoordinacion(String coordinacion) {
		this.coordinacion = coordinacion;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getCarrera() {
		return carrera;
	}
	public void setCarrera(String carrera) {
		this.carrera = carrera;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	
}
